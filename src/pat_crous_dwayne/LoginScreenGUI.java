    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // TODO add your handling code here:
        String userNameLogin = txfExistingUserName.getText();
        String userPassLogin = txfExistingUserPassword.getText();
        
        MainMenuGUI menuGUI = new MainMenuGUI();
        Login lo = new Login("","", "", userNameLogin, userPassLogin);
        
        if (lo.login() == true) {
            menuGUI.setVisible(true);
            this.setVisible(false);
        } 
    }//GEN-LAST:event_btnLoginActionPerformed

    private void btnCreateNewAccountActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateNewAccountActionPerformed
        // TODO add your handling code here:
        String userName = txfNewUserName.getText();
        String password = txfNewPassword.getText();
        String passwordConfirm = txfNewUserPasswordConfirm.getText();
        
        Login lo = new Login(userName, password, passwordConfirm, "", "");
        
        lo.writeUserDetailsToFile();
        
        txfNewUserName.setText("");
        txfNewPassword.setText("");
        txfNewUserPasswordConfirm.setText("");
    }//GEN-LAST:event_btnCreateNewAccountActionPerformed

